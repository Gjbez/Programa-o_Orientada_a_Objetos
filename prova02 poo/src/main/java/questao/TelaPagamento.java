    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
        
      if(jtcartao.isSelected() || jtdinheiro.isSelected()){
        jPanel2.setEnabled(true);
        jttotal.setEnabled(true);
        jtdesconto.setEnabled(true);
        jtpago.setEnabled(true);
        jttroco.setEnabled(true);
        jcalcular.setEnabled(true);
        jtbfinalizar.setEnabled(true);  
        jtpagar.setEnabled(true);
        
        jttotal.setText("" + comanda.calcularValorComanda());
     }
     } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Houve um problema ao aplicar os valores");
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jtpagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtpagarActionPerformed
        din = new Dinheiro(comanda, Float.parseFloat(jtpago.getText()));

        if (jtdinheiro.isSelected()) {
        jttroco.setText("" + din.calcularTroco());
        }
        else if (jtcartao.isSelected()) {
        jttroco.setText("----------------------------");
        }
    }//GEN-LAST:event_jtpagarActionPerformed

    private void jcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcalcularActionPerformed
        jtpago.setText("" + comanda.calcularValorComanda());
try{

            if (jtcartao.isSelected()) {
                if (din.calcularTotalComanda() > 50) {
            jtpago.setText("" + (comanda.calcularValorComanda() - pagar.calcularDesconto()));
            jtdesconto.setText("" + pagar.calcularDesconto());
                } else {
                    jtpago.setText("" + comanda.calcularValorComanda());
                    jtdesconto.setText("" + 0);
                }
            } else if (jtdinheiro.isSelected()) {
            jtdesconto.setText("" + din.calcularDesconto());
            }

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Houve um problema ao calcular seu troco");
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jcalcularActionPerformed

