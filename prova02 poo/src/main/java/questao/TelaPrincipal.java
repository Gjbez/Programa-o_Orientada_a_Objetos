    private void jtaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtaddActionPerformed
      try{
            
            
             
        if (jrcomida.isSelected()) {
            comanda.incluirItem(new Comida(jtxnome.getText(), Float.parseFloat(jtpreco.getText())));
        } 
        else if (jrbebida.isSelected()) {
            comanda.incluirItem(new Bebida(jtxnome.getText(), Float.parseFloat(jtpreco.getText())));
        } 
        else {
            comanda.incluirItem(new Couvert(jtxnome.getText(), Float.parseFloat(jtpreco.getText())));
        }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Houve um problema ao incluir seu item");
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jtaddActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jtlivre.isSelected() || jrquilo.isSelected()){
        jPanel2.setEnabled(true);
        jtxnome.setEnabled(true);
        jtpreco.setEnabled(true);
        jrcomida.setEnabled(true);
        jrbebida.setEnabled(true);
        jrcouvert.setEnabled(true);
        jtadd.setEnabled(true);
        jtbfinalizar.setEnabled(true);
        }
        
         if (jtlivre.isSelected()){
                comanda = new Comanda(TipoBuffet.LIVRE);
            }
             else if (jrquilo.isSelected()){
                comanda = new Comanda(TipoBuffet.QUILO);
            }else{
             JOptionPane.showMessageDialog(this, "Houve um problema com o tipo da comanda");
             }
    }//GEN-LAST:event_jButton1ActionPerformed

